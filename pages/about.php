  <h1>About xoxiiilama</h1>
  <p>xoxiiilama is your universe of eBooks. We bring together millions of titles from across the web, making it easy to discover, compare, and find your next great read.</p>

  <h2>Our Mission</h2>
  <p>We believe everyone deserves access to great books. Whether you're looking for the latest bestseller, a timeless classic, or a free read — xoxiiilama helps you find it faster.</p>

  <h2>How It Works</h2>
  <p>xoxiiilama aggregates book data from public sources including <a href="https://openlibrary.org" target="_blank">Open Library</a>. When you find a book you love, we link you to trusted retailers where you can purchase or download it. Some links may be affiliate links, meaning we earn a small commission at no extra cost to you.</p>

  <h2>Book Covers & Data</h2>
  <p>Book covers and metadata are sourced from Open Library's public API. We're grateful for their mission to make book data freely available.</p>

